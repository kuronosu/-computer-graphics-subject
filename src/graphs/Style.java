package graphs;

import java.awt.Color;

/**
 *
 * @author kuro
 */
public class Style {
    private int type;
    private Color color;
    
    public Style(int type, Color color) {
        this.type = type;
        this.color = color;
    }

    /**
     * @return the type
     */
    public int getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(int type) {
        this.type = type;
    }

    /**
     * @return the color
     */
    public Color getColor() {
        return color;
    }

    /**
     * @param color the color to set
     */
    public void setColor(Color color) {
        this.color = color;
    }
    
}
